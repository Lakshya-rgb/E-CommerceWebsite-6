package edu.ben.model;

public class User {

	private int userID;
	private String firstName;
	private String lastName;
	private String email;
	private String password;
	private int admin;
	private long lastLogin;
	private int failedAttempts;
	private int active;

	public User() {

	}

	public User(String firstName, String lastName, String email, String password, int admin, int active) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.password = password;
		this.admin = admin;
		this.active = active;
	}

	public User(int userID, String firstName, String lastName, String email, String password, int admin, long lastLogin,
			int failedAttempts, int active) {

		this.userID = userID;
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.password = password;
		this.admin = admin;
		this.lastLogin = lastLogin;
		this.failedAttempts = failedAttempts;
		this.active = active;
	}

	// Getters and setters
	
	

	public String getFirstName() {
		return firstName;
	}

	public int getActive() {
		return active;
	}

	public void setActive(int active) {
		this.active = active;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public long getLastLogin() {
		return lastLogin;
	}

	public void setLastLogin(long lastLogin) {
		this.lastLogin = lastLogin;
	}

	public int getFailedAttempts() {
		return failedAttempts;
	}

	public void setFailedAttempts(int failedAttempts) {
		this.failedAttempts = failedAttempts;
	}

	public int getAdmin() {
		return admin;
	}

	public void setAdmin(int admin) {
		this.admin = admin;
	}

	public int getUserID() {
		return userID;
	}

	public void setUserID(int userID) {
		this.userID = userID;
	}

}
